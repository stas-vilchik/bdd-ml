import math
import csv
import tensorflow as tf
import numpy as np
from sklearn.model_selection import train_test_split

tf.set_random_seed(0)

data = []
with open('image_and_complexity.csv', 'r') as f:
    reader = csv.reader(f)
    for row in reader:
        data.append(row)

data = np.array(data)
data = data.astype(np.float)

labels = data[:, 0]
inputs = data[:, 1:]

X_train, X_test, y_train, y_test = train_test_split(inputs, labels, test_size=0.33, random_state=42)


def next_batch(num, data, labels):
    idx = np.arange(0, len(data))
    np.random.shuffle(idx)
    idx = idx[:num]
    data_shuffle = [data[i] for i in idx]
    labels_shuffle = [labels[i] for i in idx]

    return np.asarray(data_shuffle), np.asarray(labels_shuffle)


x = tf.placeholder(tf.float32, [None, 1024])  # input
y_ = tf.placeholder(tf.float32, [None, ])  # answers
lr = tf.placeholder(tf.float32)

